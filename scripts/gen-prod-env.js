#!/usr/bin/env node
/**
 * gen-prod-env.js — produce a SECRET-FREE .env.production for distribution builds.
 *
 * The packaged app loads .env.production (server.js falls back to it when .env.local
 * is absent — which it always is in a build, since .env.local is no longer bundled).
 * This copies ONLY the public allow-listed keys out of the developer's real .env.local,
 * so no client secrets / webhooks / API keys ever enter the installer.
 *
 * Run automatically by the `build` / `build:portable` / `release` npm scripts.
 */
'use strict';
const fs = require('fs');
const path = require('path');

const root = path.join(__dirname, '..');
const srcPath = path.join(root, '.env.local');
const outPath = path.join(root, '.env.production');

// Public, ship-safe values only. Everything else (TWITCH_CLIENT_SECRET,
// DISCORD_CLIENT_SECRET, BUG_REPORT_WEBHOOK, BROADCAST_API_KEY, *_WEBHOOK_SECRET,
// AZURE_*) is a secret and is intentionally dropped.
const PUBLIC_KEYS = ['DISCORD_CLIENT_ID', 'TWITCH_CLIENT_ID', 'BROADCAST_REMOTE_URL'];

function parseEnv(text) {
  const out = {};
  for (const raw of text.split(/\r?\n/)) {
    const line = raw.trim();
    if (!line || line.startsWith('#')) continue;
    const eq = line.indexOf('=');
    if (eq < 0) continue;
    const key = line.slice(0, eq).trim();
    let val = line.slice(eq + 1).trim();
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
      val = val.slice(1, -1);
    }
    out[key] = val;
  }
  return out;
}

if (!fs.existsSync(srcPath)) {
  console.error('[gen-prod-env] .env.local not found — cannot build a distributable.');
  console.error('               Create .env.local with at least DISCORD_CLIENT_ID + TWITCH_CLIENT_ID.');
  process.exit(1);
}

const env = parseEnv(fs.readFileSync(srcPath, 'utf8'));

const lines = [
  '# Auto-generated by scripts/gen-prod-env.js — DO NOT EDIT BY HAND.',
  '# Public, ship-safe config bundled into the installer. Contains NO secrets.',
  '# Secret-bearing operations (Discord token exchange, etc.) run on BROADCAST_REMOTE_URL.',
  '',
];
const kept = [];
const missing = [];
for (const key of PUBLIC_KEYS) {
  const val = env[key];
  if (val) { lines.push(`${key}=${val}`); kept.push(key); }
  else missing.push(key);
}

// BROADCAST_REMOTE_URL has a hardcoded default in server.js, so it's fine if absent here.
const hardMissing = missing.filter((k) => k !== 'BROADCAST_REMOTE_URL');
if (hardMissing.length) {
  console.error('[gen-prod-env] Missing required public keys in .env.local: ' + hardMissing.join(', '));
  console.error('               Discord/Twitch login will not work in the build without them.');
  process.exit(1);
}

fs.writeFileSync(outPath, lines.join('\n') + '\n', 'utf8');
console.log('[gen-prod-env] wrote .env.production with public keys: ' + kept.join(', '));
if (missing.includes('BROADCAST_REMOTE_URL')) {
  console.log('[gen-prod-env] (BROADCAST_REMOTE_URL omitted — server.js default https://www.namelessesports.com will be used)');
}
console.log('[gen-prod-env] secrets were NOT copied (login token exchange happens on the backend).');
